blocks = raw_text.strip().split("Share\n")
posts = []

for block in blocks:
    lines = [L.strip() for L in block.strip().split('\n') if L.strip() != '']
    if len(lines) < 6: continue
    
    # Try to parse properties
    # Usually:
    # 0: Title
    # 1: u/User [avatar] (optional)
    # 2: u/User
    # 3: •
    # 4: Time (e.g. 7 days ago)
    # 5: Title (again)
    # 6...: Body (until Upvote)
    
    # Let's find "Upvote", "Downvote"
    try:
        upvote_idx = lines.index("Upvote")
        downvote_idx = lines.index("Downvote")
    except ValueError:
        continue
        
    upvotes = lines[upvote_idx + 1]
    downvotes = lines[downvote_idx + 1]
    
    title = lines[0]
    
    # find author
    author = ""
    for L in lines:
        if L.startswith("u/") and not "avatar" in L:
            author = L
            break
            
    # find time (after •)
    time_posted = ""
    try:
        dot_idx = lines.index("•")
        time_posted = lines[dot_idx + 1]
    except:
        pass
        
    # body is everything after second title to "Upvote"
    # usually title is at 0, then after time_posted is title again
    # let's just find the second occurrence of title if it exists
    body_lines = []
    start_body = False
    
    # A bit more robust approach:
    # After the time, the next line is usually title, then body starts.
    if dot_idx > 0 and len(lines) > dot_idx + 2:
        if lines[dot_idx + 2] == title:
            body_start_idx = dot_idx + 3
        else:
            body_start_idx = dot_idx + 2
            
        body_lines = lines[body_start_idx:upvote_idx]
        
    body = " ".join(body_lines)
    
    posts.append({
        "title": title,
        "author": author,
        "time": time_posted,
        "body": body,
        "upvotes": upvotes,
        "downvotes": downvotes
    })

with open("scraped_posts.json", "w") as f:
    json.dump(posts, f, indent=4)
